#!/usr/bin/env python3
# basic_operations.py
# Simple arithmetic with two predefined numbers

# Assign values
number1 = 10
number2 = 5

# Perform calculations
addition = number1 + number2
subtraction = number1 - number2   # number1 minus number2
multiplication = number1 * number2

# Print results in the required human-readable format
print(f"Addition of {number1} and {number2} is {addition}")
print(f"Subtraction of {number1} and {number2} is {subtraction}")
print(f"Multiplication of {number1} and {number2} is {multiplication}")
